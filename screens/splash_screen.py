TAGLINE = "‚Ėł Striver's A2Z DSA Sheet ‚ÄĒ keyboard-first progress tracker"


class SplashScreen(Screen):
    """2-second ASCII art splash before main screen."""

    def compose(self) -> ComposeResult:
        yield Static(
            f"[bold #00ff88]{ASCII_ART}[/]",
            id="splash-art",
        )
        yield Static(
            f"[#484f58]{TAGLINE}[/]",
            id="splash-sub",
        )

    def on_mount(self) -> None:
        self.set_timer(1.5, self._go_main)

    def _go_main(self) -> None:
        self.app.switch_screen("main")
